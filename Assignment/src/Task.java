public class Task {
    private int taskId;
    private String description;
    private String state;

    public int getTaskId(){
        return taskId;
    }

    public void setTaskId(int taskId){
        this.taskId = taskId;
    }

    public String getDescription(){
        return description;
    }

    public String getState(){
        return state;
    }
}
